<script>
	import { scaleLinear } from 'd3-scale';
	import Config from './Config.svelte';

	const points = [
		{ year: "Reid", birthrate: 16.7 },
		{ year: "Total", birthrate: 300 },
		{ year: "Charlie", birthrate: 14.4 },
	];

	const xTicks = ["Reid", "Total", "Charlie"];
	const yTicks = [0, 5, 10, 15, 20];
	const padding = { top: 20, right: 15, bottom: 20, left: 25 };

	let width = 500;
	let height = 200;

	function formatMobile(tick) {
		return "'" + tick.toString().slice(-2);
	}

	$: xScale = scaleLinear()
		.domain([0, xTicks.length])
		.range([padding.left, width - padding.right]);

	$: yScale = scaleLinear()
		.domain([0, Math.max.apply(null, yTicks)])
		.range([height - padding.bottom, padding.top]);

	$: innerWidth = width - (padding.left + padding.right);
	$: barWidth = innerWidth / xTicks.length;
</script>

<main>
	<h1>Marathon Tracker</h1>
	<p>Charlie and Reid's journey to run <a href="none">Badger Dash</a></p>
</main>

<div class="chart" bind:clientWidth={width} bind:clientHeight={height}>
	<svg>
		<!-- y axis -->
		<g class="axis y-axis">
			{#each yTicks as tick}
				<g class="tick tick-{tick}" transform="translate(0, {yScale(tick)})">
					<line x2="100%"></line>
					<text y="-4">{tick} {tick === 20 ? ' miles' : ''}</text>
				</g>
			{/each}
		</g>

		<!-- x axis -->
		<g class="axis x-axis">
			{#each points as point, i}
				<g class="tick" transform="translate({xScale(i)},{height})">
					<text x="{barWidth/2}" y="-4">{width > 380 ? point.year : formatMobile(point.year)}</text>
				</g>
			{/each}
		</g>

		<g class='bars'>
			{#each points as point, i}
				<rect
					x="{xScale(i) + 2}"
					y="{yScale(point.birthrate)}"
					width="{barWidth - 4}"
					height="{yScale(0) - yScale(point.birthrate)}"
				></rect>
			{/each}
		</g>
	</svg>
	<Config />
</div>

